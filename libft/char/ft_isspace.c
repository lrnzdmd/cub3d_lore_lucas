#include <libft.h>

int	ft_isspace(int c)
{
	return ((c ^ ' ') == 0 || (c ^ '\t') == 0
		|| (c ^ '\n') == 0 || (c ^ '\r') == 0
		|| (c ^ '\v') == 0 || (c ^ '\f') == 0);
}
